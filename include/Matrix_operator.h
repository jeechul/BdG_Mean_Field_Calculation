template<typename T>
class Matrix // Based on row major
{
	std::vector<T> mat;
	int rowN, colN;
public:
	Matrix(const int& rowN, const int& colN):rowN(rowN),colN(colN)
	{
		mat.resize(rowN*colN,0);
	}

	Matrix(const int& N):rowN(N), colN(N)
	{
		mat.resize(rowN*colN,0);
	}

	Matrix(const std::vector<T>& vec, const int& ld):rowN(ld)
	{
		int vecSize = vec.size();

		if (vecSize%rowN == 0){ colN = vecSize/rowN; }
		else{ std::cerr<<"wrong size!"<<std::endl; exit(1); }

		mat = vec;
	}

	inline T& operator[](int idx) { return mat[idx]; }

	inline T operator[](int idx) const { return mat[idx]; }

	inline T& operator()(int i, int j) { return mat[i*colN+j]; }

	inline T operator()(int i, int j) const { return mat[i*colN+j]; }

	inline int Nr() const { return rowN; }
	inline int Nc() const { return colN; }

	inline std::vector<T>& data() { return mat; }
	inline std::vector<T> data() const { return mat; }

	Matrix& operator=(const std::vector<T>& A)
	{
		std::copy(A.begin(),A.end(),mat.begin());

		return *this;
	}

	Matrix& operator=(const Matrix& A)
	{
		std::copy(A.mat.begin(),A.mat.end(),mat.begin());

		return *this;
	}

    template<typename U>
    Matrix operator+(const std::vector<U>& A) const {
		Matrix temp(rowN,colN);		

		for(int i=0;i<A.size();i++){
			temp(i,i) = mat(i,i) + A[i];
		}		

		return temp;
	}
	
    template<typename U>
	Matrix operator-(const std::vector<U>& A) const {
		Matrix temp(rowN,colN);		

		for(int i=0;i<A.size();i++){
			temp(i,i) = mat(i,i) - A[i];
		}		

		return temp;
	}

	Matrix operator+(const Matrix& A) const {
		if(rowN!=A.Nr() || colN!=A.Nc()){ std::cerr<<"wrong size!"<<std::endl; exit(1); }
		
		Matrix temp(rowN,colN);		

		for(int i=0;i<rowN*colN;i++){
			temp[i] = mat[i] + A[i];
		}		

		return temp;
	}
	
	Matrix operator-(const Matrix& A) const {
		if(rowN!=A.Nr() || colN!=A.Nc()){ std::cerr<<"wrong size!"<<std::endl; exit(1); }
	
		Matrix temp(rowN,colN);		

		for(int i=0;i<rowN*colN;i++){
			temp[i] = mat[i] - A[i];
		}		

		return temp;
	}

	Matrix operator*(const Matrix& A) const {
		if(rowN!=A.Nr() || colN!=A.Nc()){ std::cerr<<"wrong size!"<<std::endl; exit(1); }
	
		Matrix temp(rowN,colN);		

		for(int i=0;i<rowN*colN;i++){
			temp[i] = mat[i] * A[i];
		}		

		return temp;
	}

	Matrix operator/(const Matrix& A) const {
		if(rowN!=A.Nr() || colN!=A.Nc()){ std::cerr<<"wrong size!"<<std::endl; exit(1); }
	
		Matrix temp(rowN,colN);		

		for(int i=0;i<rowN*colN;i++){
			temp[i] = mat[i] / A[i];
		}		

		return temp;
	}

    template<typename U>
	Matrix operator+(const U& num) const {
		Matrix temp(rowN,colN);		

		for(int i=0;i<rowN*colN;i++){
			temp[i] = mat[i] + num;
		}		

		return temp;
	}

    template<typename U>
	Matrix operator-(const U& num) const {
		Matrix temp(rowN,colN);		

		for(int i=0;i<rowN*colN;i++){
			temp[i] = mat[i] - num;
		}		

		return temp;
	}

    template<typename U>
	Matrix operator*(const U& num) const {
		Matrix temp(rowN,colN);		

		for(int i=0;i<rowN*colN;i++){
			temp[i] = mat[i] * num;
		}		

		return temp;
	}

    template<typename U>
	Matrix operator/(const U& num) const {
		Matrix temp(rowN,colN);		

		for(int i=0;i<rowN*colN;i++){
			temp[i] = mat[i] / num;
		}		

		return temp;
	}

	Matrix& operator+=(const Matrix& A)
	{
		if(rowN!=A.Nr() || colN!=A.Nc()){ std::cerr<<"wrong size!"<<std::endl; exit(1); }
		
		for(int i=0;i<rowN*colN;i++){
			mat[i] += A[i];
		}		

		return *this;
	}

	Matrix& operator-=(const Matrix& A)
	{
		if(rowN!=A.Nr() || colN!=A.Nc()){ std::cerr<<"wrong size!"<<std::endl; exit(1); }
	
		for(int i=0;i<rowN*colN;i++){
			mat[i] -= A[i];
		}		

		return *this;
	}

	Matrix& operator*=(const Matrix& A)
	{
		if(rowN!=A.Nr() || colN!=A.Nc()){ std::cerr<<"wrong size!"<<std::endl; exit(1); }
	
		for(int i=0;i<rowN*colN;i++){
			mat[i] *= A[i];
		}		

		return *this;
	}

	Matrix& operator/=(const Matrix& A)
	{
		if(rowN!=A.Nr() || colN!=A.Nc()){ std::cerr<<"wrong size!"<<std::endl; exit(1); }
	
		for(int i=0;i<rowN*colN;i++){
			mat[i] /= A[i];
		}		

		return *this;
	}

	Matrix zgemm(dcomplex alpha, Matrix B, dcomplex beta=0.0) 
	{
		int m = rowN, n = B.colN, k = colN;
		Matrix C(m,n);

		cblas_zgemm(CblasRowMajor,CblasNoTrans,CblasNoTrans,m,n,k,&alpha,&mat[0],k,&B[0],n,&beta,&C[0],n);
		return C;
	}

	Matrix zhemm(dcomplex alpha, Matrix B, dcomplex beta=0.0)
	{
		int m = rowN, n = B.colN;
		Matrix C(m,n);

		cblas_zhemm(CblasRowMajor,CblasLeft,CblasLower,m,n,&alpha,&mat[0],m,&B[0],m,&beta,&C[0],m);
		return C;
	}

	Matrix dgemm(double alpha, Matrix B, double beta=0.0)
	{
		int m = rowN, n = B.colN, k = colN;
		Matrix C(m,n);

		cblas_dgemm(CblasRowMajor,CblasNoTrans,CblasNoTrans,m,n,k,alpha,&mat[0],k,&B[0],n,beta,&C[0],m);
		return C;
	}

	Matrix dsymm(double alpha, Matrix B, double beta=0.0)
	{
		int m = rowN, n = B.colN;
		Matrix C(m,n);

		cblas_dsymm(CblasRowMajor,CblasLeft,CblasLower,m,n,alpha,&mat[0],m,&B[0],m,beta,&C[0],m);
		return C;
	}

	Matrix& minus() 
	{
		for(int i=0;i<rowN*colN;i++) mat[i] *= -1.0;
		return *this;
	}
	
	Matrix& atomic_inverse() 
	{
		for(int i=0;i<rowN*colN;i++){
			mat[i] = 1./mat[i];
		}
		return *this;
	}

	Matrix transpose() const {
		Matrix temp(colN,rowN);

		for (int i=0; i<colN; i++){
			for (int j=0; j<rowN; j++){
				temp(i,j) = mat[j*colN+i];
			}
		}

		return temp;
	}

	Matrix Hermitian_transpose() const {
		Matrix temp(colN,rowN);

		for (int i=0; i<colN; i++){
			for (int j=0; j<rowN; j++){
				temp(i,j) = std::conj(mat[j*colN+i]);
			}
		}

		return temp;
	}

	template<typename U>
	void fill_diagonal(const U& num, int ri=0, int ci=0){
		int diag_size = std::min(rowN-ri,colN-ci);
		for(int i=0;i<diag_size;i++) mat[(i+ri)*colN+i+ci] = num;
	}

	template<typename U>
	void fill_diagonal(const std::vector<U>& diag, int ri=0, int ci=0){
		int diag_size = diag.size();
		for(int i=0;i<diag_size;i++) mat[(i+ri)*colN+i+ci] = diag[i];
	}

	void drop_off_diag(int ri, int rf, int ci, int cf){
		for(int i=ri;i<rf;i++)
		for(int j=ci;j<cf;j++){
			if( (i-ri)!=(j-ci) ) mat[i*colN+j] = 0;
		}
	}

	std::vector<T> diag(){
		std::vector<T> temp(rowN);

		for(int i=0;i<rowN;i++) temp[i] = mat[i*colN+i];

		return temp;
	}

	void put_Block(const Matrix& A, int ri, int rf, int ci, int cf)
	{
		for(int i=ri;i<rf;i++)
		for(int j=ci;j<cf;j++)
			mat[i*colN+j] = A(i-ri,j-ci);
	}

	void add_Block(const Matrix& A, int ri, int rf, int ci, int cf)
	{
		for(int i=ri;i<rf;i++)
		for(int j=ci;j<cf;j++)
			mat[i*colN+j] += A(i-ri,j-ci);
	}

	void sub_Block(const Matrix& A, int ri, int rf, int ci, int cf)
	{
		for(int i=ri;i<rf;i++)
		for(int j=ci;j<cf;j++)
			mat[i*colN+j] -= A(i-ri,j-ci);
	}

	Matrix pull_Block(int ri, int rf, int ci, int cf)
	{
		Matrix temp(rowN,colN);

		for(int i=ri;i<rf;i++)
		for(int j=ci;j<cf;j++)
			temp(i-ri,j-ci) = mat[i*colN+j];

		return temp;
	}

	void view_mat(int ri, int rf, int ci, int cf)
	{
		for(int i=ri;i<rf;i++){
			for(int j=ci;j<cf;j++) std::cout<<(*this)(i,j)<<" "; std::cout<<std::endl;
		}
		std::cout<<std::endl;
	}	

	void clear(){
		mat.clear(); mat.resize(rowN*colN,0); 
	};	
};

using MatrixC = Matrix<dcomplex>;
