template<typename T>
T arrmul(vector<T> a, vector<T> b)
{
	T temp = 0.0;
	for(int i=0;i<a.size();i++){ 
		temp += a[i]*b[i];
	}
	return temp;
};

template<>
dcomplex arrmul(vector<dcomplex> a, vector<dcomplex> b)
{
	dcomplex temp = 0.0;
	for(int i=0;i<a.size();i++){ 
		temp += conj(a[i])*b[i];
	}
	return temp;
};

